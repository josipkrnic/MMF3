import jednostavnonjihalo as nj
import numpy
import matplotlib.pyplot as plt
t,y = nj.analiticki(4)
T = nj.analiticki_period()
a,b = nj.euler(4,0,0,20*T,50000)
c,d = nj.euler(4,0,0,20*T,100000)
e,f = nj.euler(4,0,0,20*T,250000)
g,h = nj.euler(4,0,0,20*T,500000)
i,j = nj.euler(4,0,0,20*T,10000)
K,L = nj.euler(4,0,0,20*T,2000)
plt.subplot(1,2,1)
plt.plot(t,y,c="r")
plt.scatter(K,L,c="yellow")
plt.scatter(i,j,c="black")
plt.scatter(a,b,c="b")
plt.scatter(c,d,c="orange")
plt.scatter(e,f,c="g")
plt.scatter(g,h,c="cyan")
plt.xlim(0,T)
plt.legend(["Analitički","N = 2000","N = 10000","N = 50000","N = 100000", "N = 250000","N = 500000"],loc="upper right")
plt.subplot(1,2,2)
plt.plot(t,y,c="r")
